//sec03 hover
//sec03 : journey01 부분
var title_Ahover1 = $(".journey01 >ul >li.j-title1 a");
title_Ahover1.hover(
  function () {
    $(".journey01 >ul >li.j-title1").css({
      background: "#e11425",
    });
    title_Ahover1.css({
      color: "#fff",
      "text-decoration": "underline",
    });
  },
  function () {
    $(".journey01 >ul >li.j-title1").css({
      background: "#fff",
    });
    title_Ahover1.css({
      color: "#161616",
      "text-decoration": "none",
    });
  }
);

